export const GLYPH_W = 5;
export const GLYPH_H = 7;

function glyph(ch: string): Glyph {
  return F[ch] ?? F[ch.toUpperCase()] ?? F["?"];
}

/** 한 글자를 (x,y) 좌상단 기준, scale배 픽셀로 렌더. */
export function drawChar(
  ctx: CanvasRenderingContext2D,
  ch: string,
  x: number,
  y: number,
  scale: number,
  color: string
): void {
  const g = glyph(ch);
  ctx.fillStyle = color;
  for (let row = 0; row < GLYPH_H; row++) {
    const bits = g[row];
    for (let col = 0; col < GLYPH_W; col++) {
      if (bits[col] === "1" || bits[col] === "#") {
        ctx.fillRect(x + col * scale, y + row * scale, scale, scale);
      }
    }
  }
}

export function textWidth(text: string, scale: number, letterSpacing = 1): number {
  if (text.length === 0) return 0;
  return text.length * (GLYPH_W + letterSpacing) * scale - letterSpacing * scale;
}

/** 문자열 렌더. 반환값은 전체 폭. */
export function drawText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  scale: number,
  color: string,
  letterSpacing = 1
): number {
  let cx = x;
  const step = (GLYPH_W + letterSpacing) * scale;
  for (const ch of text) {
    drawChar(ctx, ch, cx, y, scale, color);
    cx += step;
  }
  return cx - x;
}

/** 가운데 정렬 렌더. cx는 중심 x. */
export function drawTextCentered(
  ctx: CanvasRenderingContext2D,
  text: string,
  cx: number,
  y: number,
  scale: number,
  color: string,
  letterSpacing = 1
): void {
  const w = textWidth(text, scale, letterSpacing);
  drawText(ctx, text, Math.round(cx - w / 2), y, scale, color, letterSpacing);
}
